/**
* Holds the global and local coordinate locations of a spatial
* position in a particular view.  All coordinates are stored as
* double-precision values.  This class used to also store 
* integer-precision versions of the local coordinate points.  Now
* that Java2D directly supports resolution-independent rendering,
* the integer-precision values are no longer needed.
* @author Thorn Green
*/
public class Hexar extends Meta<Hexar> {
	public final void datCpy(Hexar out) {
		out.getLoc().set(loc.x, loc.y);
		glo.mcpy(out.getGlo());
	};
	public Hexar copyNode() {
		Hexar temp = new Hexar();
		datCpy(temp);
		return (temp);
	};
	public Hexar copySub() {
		Hexar temp = new Hexar();
		datCpy(temp);
		return (temp);
	};
	public Hexar copyAll() {
		Hexar temp = new Hexar();
		datCpy(temp);
		return (temp);
	};
	public void copyNodeInfo(Hexar in) {
		Hexar temp = in;
		datCpy(temp);
	};
	public void copySubInfo(Hexar in) {
		Hexar temp = in;
		datCpy(temp);
	};
	public void copyAllInfo(Hexar in) {
		Hexar temp = in;
		datCpy(temp);
	};
	public void eraseNode() {};
	public void eraseSub() {};
	public void eraseAll() {};

	/**
	* Gets the multivector holding the global coordinate location.
	*/
	public final Mvec getGlo() {
		return (glo);
	}
	/**
	* Sets the multivector holding the global coordinate location.
	* This allows the same global coordinate location to be shared
	* by multiple local coordinate locations in different views.
	*/
	public final void setGlo(Mvec in) {
		glo = in;
	}
	/**
	* Gets the local coordinate X-value.
	*/
	public final double getPtx() {
		return (loc.x);
	}
	/**
	* Sets the local coordinate X-value.
	*/
	public final void setPtx(double in) {
		loc.set((float)in, loc.y);
	}
	/**
	* Gets the local coordinate Y-value.
	*/
	public final double getPty() {
		return (loc.y);
	}
	/**
	* Sets the local coordinate Y-value.
	*/
	public final void setPty(double in) {
		loc.set(loc.x, (float)in);
	}
	/**
	* Gets the local coordinate point.
	*/
	public final PointF getLoc() {
		return (loc);
	}

	private Mvec glo = new Mvec();
	private final PointF loc = new PointF();

	public void wake() {};

}
