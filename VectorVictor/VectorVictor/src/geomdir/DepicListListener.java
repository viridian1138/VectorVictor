/**
* Interface implemented by all classes that provide a "view" of
* a set of depictors, and/or have a value that updates based on
* the dragging of a depictor.  The model calls methods on this
* interface to inform the view of changes in the state of the model.
* @author Thorn Green
*/
public interface DepicListListener {
	/**
	* Informs the view that the identity of the last depictor clicked has changed.
	*/
	public void localSetLastClickDepic(DrawObj InDrw);
	/**
	* Instructs the view to pull a paritcular depictor from its drawing list, if possible.
	*/
	public DGMNode localPullFromDrawingList(DrawObj MyObj);
	/**
	* Informs the view of a new delegation event that may require it to change
	* the set of depictors in its drawing list.
	*/
	public void localDelegationEvent(boolean Add, DrawObj FromObj, DrawObj ToObj);
	/**
	* Brings a depictor to the front of the drawing list, if possible.
	*/
	public void localBringToFront(DrawObj MyObj);
	/**
	* Sends a depictor to the back of the drawing list, if possible.
	*/
	public void localSendToBack(DrawObj MyObj);
	/**
	* Requests that the view appends a particular depictor to the drawing list.
	*/
	public void localDisplayAppend(DrawObj MyObj);
	/**
	* Synchronously paints the view.
	*/
//	public void localSynchPaint();
	/**
	* Informs the view that the model has been cleared.  The view
	* must get rid of any information dependent on that model, like
	* its list of depictors.
	*/
	public void localClearModel();
	/**
	* Repaints the view.
	*/
	public void invalidate(); // postInvalidate();

	/**
	* Updates all depictors in the view.
	*/
	public void updateYourself1();
	/**
	* Informs the depictor of a drag event in its view.  Sends the coordinate context
	* of the depictor involved, if applicable.
	*/
	public void clickAndDrag(MotionEvent e, CoordContext PrtCon);
	/**
	* Gets the radius of the coordinate system.  Used to compute solver tolerances.
	* A class implementing this interface can indicate non-support for a coordinate
	* radius by returning a negative value.
	*/
	public double getCoordRad();
}
